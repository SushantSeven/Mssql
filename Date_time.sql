--Date time functions

--DET DATE
SELECT GETDATE() AS _GETDATE; --FORMAT YYYY-MM-DD HH-MM-SS-MS

--SYSTEM DATE
SELECT SYSDATETIME() AS _SYSDATETIME; --FORMAT YYYY-MM-DD HH-MM-SS-MS GETS THE SYSTEM DATE

--CURRENT DATE
SELECT CURRENT_TIMESTAMP AS _CURRENT_TIMESTAMP; --FORMAT YYYY-MM-DD HH-MM-SS-MS GETS THE CURRENT DATE

-- GETTING YEAR MNTH AND DAY
SELECT 
	YEAR('2024/05/01 14:38:52') AS _year, 
	MONTH('2024/05/01 14:38:52') AS _month, 
	DAY('2024/05/01 14:38:52') AS _day;

-- GETTING YEAR MONTH AND DAY IN INTEGER
SELECT 
	DATEPART(YEAR, '2024/05/01 14:38:52') AS _year, 
	DATEPART(MONTH, '2024/05/01 14:38:52') AS _month, 
	DATEPART(DAY, '2024/05/01 14:38:52') AS _day, 
	DATEPART(HOUR, '2024/05/01 14:38:52') AS _hour, 
	DATEPART(MINUTE, '2024/05/01 14:38:52') AS _minute, 
	DATEPART(SECOND, '2024/05/01 14:38:52') AS _second;

-- GETTING YEAR MONTH AND DAY IN STRING
SELECT 
	DATENAME(YEAR, '2024/05/01 14:38:52') AS _year, 
	DATENAME(MONTH, '2024/05/01 14:38:52') AS _month, 
	DATENAME(WEEKDAY, '2024/05/01 14:38:52') AS _day, 
	DATENAME(HOUR, '2024/05/01 14:38:52') AS _hour, 
	DATENAME(MINUTE, '2024/05/01 14:38:52') AS _minute, 
	DATENAME(SECOND, '2024/05/01 14:38:52') AS _second;

-- RETURNS LAST DATE OF A MONTH
SELECT EOMONTH('2024-06-01') AS _eom;